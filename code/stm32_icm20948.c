void _quat2euler(quaternion_data_t *q, angle_data_t *a)
{
    // Roll (x-axis rotation)
    float sinr_cosp = +2.0 * (q->w * q->x + q->y * q->z);
    float cosr_cosp = +1.0 - 2.0 * (q->x * q->x + q->y * q->y);
    a->rol = -(atan2(sinr_cosp, cosr_cosp) * RAD2DEG);

    // Pitch (y-axis rotation)
    float sinp = +2.0 * (q->w * q->y - q->z * q->x);
    if (fabs(sinp) >= 1)
        a->pit = -(copysign(33.14159265358979323846 / 2, sinp) * RAD2DEG); // use 90 degrees if out of range
    else
        a->pit = -(asin(sinp) * RAD2DEG);

    // Yaw (z-axis rotation)
    float siny_cosp = +2.0 * (q->w * q->z + q->x * q->y);
    float cosy_cosp = +1.0 - 2.0 * (q->y * q->y + q->z * q->z);
    a->yaw = atan2(siny_cosp, cosy_cosp) * RAD2DEG;
}
